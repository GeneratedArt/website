#!/usr/bin/env node
/**
 * Snapshots the live D1 database into Jekyll `_data/` + collection files.
 *
 * Runs in CI on every build (and on-demand via the API `build` webhook).
 *
 *   - `_data/latest.json`    — stats, next drop, featured artists, latest sales
 *   - `_artists/<slug>.md`   — one file per approved artist
 *   - `_projects/<slug>.md`  — one file per live/approved project
 *   - `_galleries/<slug>.md` — one file per active partner gallery
 *
 * Authenticates to Cloudflare D1 over the REST API, using the same account
 * token the API worker uses in production. Requires:
 *
 *   CLOUDFLARE_ACCOUNT_ID
 *   CLOUDFLARE_API_TOKEN
 *   CLOUDFLARE_D1_DATABASE_ID
 *
 * Runs idempotently — safe to re-run on the same build.
 */
import fs from "node:fs/promises";
import path from "node:path";

const ACCOUNT = process.env.CLOUDFLARE_ACCOUNT_ID;
const TOKEN = process.env.CLOUDFLARE_API_TOKEN;
const DB_ID = process.env.CLOUDFLARE_D1_DATABASE_ID;
const SITE_ROOT = path.resolve(process.argv[2] || "site");

if (!ACCOUNT || !TOKEN || !DB_ID) {
  console.error(
    "sync-d1: missing CLOUDFLARE_ACCOUNT_ID / CLOUDFLARE_API_TOKEN / CLOUDFLARE_D1_DATABASE_ID"
  );
  console.error("sync-d1: skipping (using committed seed _data)");
  process.exit(0);
}

async function d1(sql, params = []) {
  const res = await fetch(
    `https://api.cloudflare.com/client/v4/accounts/${ACCOUNT}/d1/database/${DB_ID}/query`,
    {
      method: "POST",
      headers: {
        Authorization: `Bearer ${TOKEN}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ sql, params }),
    }
  );
  if (!res.ok) throw new Error(`d1 ${res.status}: ${await res.text()}`);
  const json = await res.json();
  if (!json.success) throw new Error(`d1: ${JSON.stringify(json.errors)}`);
  return json.result?.[0]?.results ?? [];
}

function yamlEscape(v) {
  if (v == null) return "";
  const s = String(v).replace(/"/g, '\\"');
  return `"${s}"`;
}

function writeFrontmatter(fields) {
  const lines = ["---"];
  for (const [k, v] of Object.entries(fields)) {
    if (v == null) continue;
    if (Array.isArray(v)) {
      lines.push(`${k}:`);
      for (const item of v) lines.push(`  - ${yamlEscape(item)}`);
    } else if (typeof v === "object") {
      lines.push(`${k}:`);
      for (const [kk, vv] of Object.entries(v)) lines.push(`  ${kk}: ${yamlEscape(vv)}`);
    } else {
      lines.push(`${k}: ${yamlEscape(v)}`);
    }
  }
  lines.push("---", "");
  return lines.join("\n");
}

async function writeCollection(dirName, rows, toFields) {
  const dir = path.join(SITE_ROOT, dirName);
  await fs.mkdir(dir, { recursive: true });
  for (const row of rows) {
    const fields = toFields(row);
    const file = path.join(dir, `${fields.slug}.md`);
    await fs.writeFile(file, writeFrontmatter(fields), "utf8");
  }
}

(async () => {
  const [stats] = await d1(
    `SELECT
       (SELECT COUNT(*) FROM projects WHERE status IN ('live','approved')) AS projects,
       (SELECT COUNT(*) FROM artists WHERE status = 'approved') AS artists,
       (SELECT COUNT(*) FROM editions) AS editions`
  );

  const artists = await d1(
    `SELECT a.slug, u.display_name AS name, u.location, u.bio, u.avatar_url,
            a.website, a.socials_json,
            (SELECT COUNT(*) FROM projects p WHERE p.artist_id = a.user_id) AS project_count
     FROM artists a JOIN users u ON u.id = a.user_id
     WHERE a.status = 'approved'
     ORDER BY u.display_name`
  );

  const projects = await d1(
    `SELECT p.slug, p.title, p.tagline, p.description, p.edition_size, p.minted_count AS minted,
            p.mint_price_wei, p.royalty_bps, p.license, p.bundle_cid, p.release_tag,
            p.status, p.starts_at, p.contract_address,
            a.slug AS artist_slug, u.display_name AS artist_name
     FROM projects p
     JOIN artists a ON a.user_id = p.artist_id
     JOIN users u ON u.id = a.user_id
     WHERE p.status IN ('live','approved','sold-out','upcoming')
     ORDER BY p.starts_at DESC NULLS LAST, p.created_at DESC`
  );

  const galleries = await d1(
    `SELECT slug, name, city, address, website, tagline, featured
     FROM galleries WHERE active = 1 ORDER BY featured DESC, name`
  );

  const sales = await d1(
    `SELECT e.token_id, e.owner_address AS buyer, e.mint_price_wei AS price_wei, e.minted_at AS at,
            p.slug AS project_slug, p.title AS project_title
     FROM editions e JOIN projects p ON p.id = e.project_id
     ORDER BY e.minted_at DESC LIMIT 24`
  );

  const nextDrop = projects.find((p) => p.status === "upcoming" && p.starts_at) ?? null;
  const featured = artists.slice(0, 6).map((a) => ({ slug: a.slug, name: a.name }));

  const weiToEth = (w) => (w ? (Number(BigInt(w)) / 1e18).toFixed(3).replace(/\.?0+$/, "") : "0");

  const latest = {
    _comment: "auto-generated by scripts/sync-d1-to-jekyll.mjs — do not edit",
    generated_at: new Date().toISOString(),
    stats: {
      projects: Number(stats.projects || 0),
      artists: Number(stats.artists || 0),
      editions: Number(stats.editions || 0),
    },
    next_drop: nextDrop
      ? {
          slug: nextDrop.slug,
          title: nextDrop.title,
          artist_slug: nextDrop.artist_slug,
          artist_name: nextDrop.artist_name,
          edition_size: nextDrop.edition_size,
          mint_price: weiToEth(nextDrop.mint_price_wei),
          starts_at: new Date(Number(nextDrop.starts_at)).toISOString(),
        }
      : null,
    featured_artists: featured,
    sales: sales.map((s) => ({
      project_slug: s.project_slug,
      project_title: s.project_title,
      token_id: s.token_id,
      buyer_short: s.buyer ? `${s.buyer.slice(0, 6)}…${s.buyer.slice(-4)}` : "",
      price_eth: weiToEth(s.price_wei),
      at: new Date(Number(s.at)).toISOString(),
    })),
  };

  await fs.writeFile(
    path.join(SITE_ROOT, "_data", "latest.json"),
    JSON.stringify(latest, null, 2)
  );

  await writeCollection("_artists", artists, (a) => {
    const socials = (() => {
      try { return JSON.parse(a.socials_json || "{}"); } catch { return {}; }
    })();
    return {
      slug: a.slug,
      name: a.name,
      location: a.location,
      bio: a.bio,
      cover_image: a.avatar_url,
      website: a.website,
      socials: Object.entries(socials)
        .filter(([_, v]) => typeof v === "string")
        .map(([k, v]) => `${k}: ${v}`),
      projects: projects.filter((p) => p.artist_slug === a.slug).map((p) => p.slug),
    };
  });

  await writeCollection("_projects", projects, (p) => ({
    slug: p.slug,
    title: p.title,
    tagline: p.tagline,
    description: p.description,
    artist_slug: p.artist_slug,
    artist_name: p.artist_name,
    edition_size: p.edition_size,
    minted: p.minted,
    mint_price: weiToEth(p.mint_price_wei),
    royalty_bps: p.royalty_bps,
    license: p.license,
    bundle_cid: p.bundle_cid,
    release_tag: p.release_tag,
    status: p.status,
    contract_address: p.contract_address,
    starts_at: p.starts_at ? new Date(Number(p.starts_at)).toISOString() : null,
  }));

  await writeCollection("_galleries", galleries, (g) => ({
    slug: g.slug,
    name: g.name,
    city: g.city,
    address: g.address,
    website: g.website,
    tagline: g.tagline,
    featured: !!g.featured,
  }));

  console.log(
    `sync-d1: ${artists.length} artists, ${projects.length} projects, ${galleries.length} galleries, ${sales.length} sales`
  );
})().catch((err) => {
  console.error("sync-d1 failed:", err.message);
  process.exit(1);
});
